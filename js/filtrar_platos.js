menuPostre = platosDelMenu.filter( plato => plato.tipo == "postre")

function constructorDePlato(plato) {
    elemento = `
    <div class="container_plato" >
        <img src="./assets/Imagenes/menu/${plato.nombreArchivo}">
        <div class="plato_description">
            <h2>${plato.plato}</h2>
            <p>${plato.descripcion}</p>
            <h3>${plato.precio}</h3>
        </div>
    </div> 
    `
    section2.innerHTML += elemento
}

function construirMenu(menu) {
    section2.innerHTML = ""
    menu.forEach( plato => {
        constructorDePlato(plato)
    })
}

construirMenu(platosDelMenu)

botonMenuCompleto = document.getElementById("menu_completo")
